y = 4     # y stores 4

print(x + y)   # 11 + 4 = 15
print(x - y)   # 11 - 4 = 7
print(x * y)   # 11 * 4 = 44
print(x / y)   # 11 / 4 = 2.75   // float division
print(x // y)  # 11 // 4 = 2     // integer division
print(x % y)   # 11 % 4 = 3      // remainder
print(x ** y)  # 11 ** 4 = 14641 // to the power of

